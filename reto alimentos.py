def evaluación_de_alimentos():
  # Pedirle al usuario la cantidad de grasa de una porción de torta
  torta = float(input("Ingresa la cantidad de grasa del pan en g"))

  # Pedirle al usuario la cantidad de azúcar de un pan
  azúcar = float(input("Ingresar la cantidad de azúcar por pan en g"))

  # Pedirle al usuario la cantidad de sodio por pan
  sodio = float(input("Ingresa la cantidad de sodio por pan en g"))

  # Mostrar resultado
  print("La cantidad de grasa es:", torta)
  print("La cantidad de azúcar es:", azúcar)
  print("La cantidad de sodio es:", sodio)

# Call the function
evaluación_de_alimentos()
